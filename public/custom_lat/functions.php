	function letterCountrie($countrie){
		if($countrie == 1){ return "Colombia";
		}elseif($countrie == 2){ return "México";
		}elseif($countrie == 3){ return "Perú";
		}elseif($countrie == 4){ return "Ecuador";
		}elseif($countrie == 5){ return "Panamá";
		}elseif($countrie == 6){ return "Guatemala";
		}elseif($countrie == 7){ return "El Salvador";
		}elseif($countrie == 8){ return "Costa Rica";
		}elseif($countrie == 10){ return "Chile";
		}else{ return "Latinoamérica"; }
	}
